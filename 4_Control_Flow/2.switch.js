// switch statement 

// switch (key) {
//     case value:
        
//         break;

//     default:
//         break;
// }

// Example :

const day = "mon"  // key = value
switch (day) {
    case "mon":
        console.log("Monday")
        // break;
    case "tue":
        console.log("Tuesday")
        break;
    case "wed":
        console.log("Wednesday")
        break;
    case "thu":
        console.log("Thursday")
        // break;
    case "fri":
        console.log("Friday")
        break;
    case "sat":
        console.log("Saturday")
        break;
    case "sat":
        console.log("Sunday")
        break;

    default:
        console.log("invalid input")
        break;
}